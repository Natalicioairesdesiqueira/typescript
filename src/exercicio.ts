// Função de multiplicação
function multiplicar(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudar(nome: string): string {
    return `Olá ${nome}`;
}

// Exemplos de uso:
const resultadoMultiplicacao = multiplicar(5, 3);  // retorna 15
const saudacao = saudar("Maria");  // retorna "Olá Maria"